public class funcaoRaio {
    
    public static final double pi = 3.14159;
    
    public static double circunference(double radiun){
        
        return 2.0 * pi * radiun;
    }
    
    public static  double volume(double radiun) {
        
        return 4.0 * pi * radiun * radiun * radiun / 3.0;
    }
}
    

